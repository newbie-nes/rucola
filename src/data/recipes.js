/**
 * Get recipes filtered and scored for a user, considering fridge contents and yesterday's meal.
 * @param {Object} userProfile
 * @param {string[]} [fridgeItems] - Ingredient keys currently in the fridge
 * @param {number|null} [yesterdayRecipeId] - Recipe ID eaten yesterday (to avoid repetition)
 * @returns {Object[]} Sorted array of matching recipes with _fridgeMatch info
 */
export function getRecipesForUser(userProfile, fridgeItems = [], yesterdayRecipeId = null) {
  const difficultyRank = { easy: 1, medium: 2, hard: 3 };

  // Find yesterday's recipe for variety scoring
  const yesterdayRecipe = yesterdayRecipeId
    ? recipes.find(r => r.id === yesterdayRecipeId)
    : null;

  // Step 1: Filter by diet compatibility
  let filtered = recipes.filter((recipe) => {
    return recipe.diet.includes(userProfile.diet || "omnivore");
  });

  // Step 2: Filter by allergies
  if (userProfile.allergies && userProfile.allergies.length > 0) {
    filtered = filtered.filter((recipe) => {
      const recipeIngredientKeys = [recipe.base, recipe.vegetable, recipe.protein];
      const allIngredientTexts = [
        ...recipe.allIngredients.en.map((i) => i.toLowerCase()),
        ...recipe.allIngredients.it.map((i) => i.toLowerCase()),
      ];
      return !userProfile.allergies.some(
        (allergen) =>
          recipeIngredientKeys.includes(allergen) ||
          allIngredientTexts.some((text) => text.includes(allergen.toLowerCase()))
      );
    });
  }

  // Step 3: Filter by disliked ingredients
  if (userProfile.dislikedIngredients && userProfile.dislikedIngredients.length > 0) {
    filtered = filtered.filter((recipe) => {
      const recipeIngredientKeys = [recipe.base, recipe.vegetable, recipe.protein];
      return !userProfile.dislikedIngredients.some((disliked) =>
        recipeIngredientKeys.includes(disliked)
      );
    });
  }

  // Step 4: Filter by max difficulty
  if (userProfile.maxDifficulty) {
    const maxRank = difficultyRank[userProfile.maxDifficulty] || 3;
    filtered = filtered.filter(
      (recipe) => (difficultyRank[recipe.difficulty] || 1) <= maxRank
    );
  }

  // Step 5: Filter by max prep time
  if (userProfile.maxPrepTime) {
    filtered = filtered.filter(
      (recipe) => recipe.prepTime <= userProfile.maxPrepTime
    );
  }

  // Step 6: Score and sort
  const normalizedFridge = fridgeItems.map((item) => item.toLowerCase());

  const scored = filtered.map((recipe) => {
    let score = 0;

    // --- Fridge match: track which key ingredients are in/out of fridge ---
    const keyIngredients = [
      { type: 'base', key: recipe.base },
      { type: 'vegetable', key: recipe.vegetable },
      { type: 'protein', key: recipe.protein }
    ];

    const inFridge = [];
    const missing = [];

    keyIngredients.forEach(({ type, key }) => {
      if (normalizedFridge.some(fi => matchesKeyIngredient(fi, key))) {
        inFridge.push(key);
        score += 10;
      } else {
        missing.push(key);
      }
    });

    // Check allIngredients for partial fridge matches (fuzzy)
    recipe.allIngredients.en.forEach((ingredient) => {
      if (
        normalizedFridge.some(
          (fridgeItem) => ingredientsMatch(fridgeItem, ingredient)
        )
      ) {
        score += 3;
      }
    });

    // --- Yesterday's meal penalty (variety) ---
    if (yesterdayRecipe) {
      if (recipe.id === yesterdayRecipe.id) {
        score -= 15; // same exact recipe
      } else {
        if (recipe.protein === yesterdayRecipe.protein) score -= 8;
        if (recipe.base === yesterdayRecipe.base) score -= 5;
        if (recipe.vegetable === yesterdayRecipe.vegetable) score -= 3;
      }
    }

    // Preferred tags bonus
    if (userProfile.preferredTags && userProfile.preferredTags.length > 0) {
      recipe.tags.forEach((tag) => {
        if (userProfile.preferredTags.includes(tag)) {
          score += 5;
        }
      });
    }

    // Quick recipes get a small bonus
    if (recipe.prepTime <= 20) {
      score += 2;
    }

    // Easy recipes get a tiny bonus
    if (recipe.difficulty === "easy") {
      score += 1;
    }

    return {
      ...recipe,
      _score: score,
      _fridgeMatch: { inFridge, missing }
    };
  });

  // Step 7: Filter by max 1 missing KEY ingredient (base/vegetable/protein)
  // _fridgeMatch.missing already tracks which key ingredients are not in fridge
  const strictFiltered = scored.filter(r => r._fridgeMatch.missing.length <= 1);

  // Sort by score descending, then prepTime ascending
  const toSort = strictFiltered.length > 0 ? strictFiltered : scored;
  toSort.sort((a, b) => {
    if (b._score !== a._score) return b._score - a._score;
    return a.prepTime - b.prepTime;
  });

  // Flag recipes if no strict match found (all need >1 key ingredient)
  const noStrictMatch = strictFiltered.length === 0;

  // Keep _fridgeMatch but remove _score
  return toSort.map(({ _score, ...recipe }) => ({
    ...recipe,
    ...(noStrictMatch ? { _noStrictMatch: true } : {})
  }));
}

export default recipes;
